

exports.tinhTong = function(a,b){
    let tong = a + b;
    return tong;
}
exports.tinhHieu = function(a,b){
    let hieu = a - b;
    return hieu;
}
exports.tinhTich = function(a,b){
    let tich = a * b;
    return tich;
}
exports.tinhThuong = function(a,b){
    let thuong = a / b;
    return thuong;
}